interface IPost {
  id: string
  title: string
  body: string
}

const GET_POSTS = gql`
  query getPosts {
    posts {
      id
      title
    }
  }
`

const Posts = () => {
  const { loading, data } = useQuery(GET_POSTS)
  if (loading) {
    return <Text>Loading ...</Text>
  }

  return (
    <FlatList
      data={data.posts}
      renderItem={({ item }: { item: IPost }) => <Text>{item.title}</Text>}
      keyExtractor={item => item.id}
    />
  )
}

